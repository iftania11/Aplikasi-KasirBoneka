       public void tampil_table(){
        tm=new DefaultTableModel (null,
        new Object[] {"No","Nama pengguna","Password","Level","Email"} );
        tblpengguna.setModel(tm);
        tm.getDataVector().removeAllElements();
        try{
            PreparedStatement s= con.prepareStatement("select * from tbl_user");
            ResultSet r=s.executeQuery();
            while (r.next()){
                Object [] data={
                r.getString(1),
                r.getString(2),
                r.getString(3),
                r.getString(4),
                r.getString(5)
            };
            tm.addRow(data);
            }
        }catch (Exception e){
            System.out.println("error koneksi database" + e);
        }
       }

