    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
         try{
        PreparedStatement ps=con.prepareStatement("INSERT INTO tbl_user values (?,?,?,?,?)");
        ps.setString(1, txtno.getText());
        ps.setString(2, txtnama.getText());
        ps.setString(3, pass.getText());
        ps.setString(4, cblevel.getSelectedItem().toString());
        ps.setString(5, txtemail.getText());
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data Telah Tersimpan");
        tampil_table();
        }catch(Exception e){
            System.out.println("error koneksi database" + e);
        }               
    }//GEN-LAST:event_btntambahActionPerformed

    private void tblpenggunaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpenggunaMouseClicked
        // TODO add your handling code here:
         int  i = tblpengguna.getSelectedRow();
        if (i == -1) {
            return;
        }
        String No = (String) tm.getValueAt (i,0);
        txtno.setText(No);
        String Nama_pengguna= (String) tm.getValueAt (i, 1);
        txtnama.setText(Nama_pengguna);
        String Password= (String) tm.getValueAt (i,2);
        pass.setText(Password);
        String Level= (String) tm.getValueAt (i,3);
        cblevel.setSelectedItem(Level);
        String email= (String) tm.getValueAt (i,4);
        txtemail.setText(email);
    }//GEN-LAST:event_tblpenggunaMouseClicked

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
           try{
            PreparedStatement ps = con.prepareStatement ("UPDATE tbl_user SET nama_user=?,password=?,level=?,email=? WHERE id_pengguna=?");
            ps.setString(1, txtnama.getText());
            ps.setString(2, pass.getText());
            ps.setString(3, cblevel.getSelectedItem().toString());
            ps.setString(4, txtemail.getText());
            ps.setString(5, txtno.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah di Update");
            tampil_table();
        }catch(Exception e){
            System.out.print("error koneksi database" + e); 
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
         int ok= JOptionPane.showConfirmDialog(null,"Anda yakin ingin menghapus?='"+
               txtno.getText()+"'","punten",JOptionPane.YES_NO_OPTION);
        if (ok==0) {
        try {
        PreparedStatement ps = con.prepareStatement("DELETE FROM tbl_user WHERE id_pengguna=?");
        ps.setString(1, txtno.getText());
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "DATA telah di Delete");
        tampil_table();
        } catch (Exception e) {
         System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

