    private void tblkategoriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblkategoriMouseClicked
        // TODO add your handling code here:
        int i = tblkategori.getSelectedRow();
        if (i == -1) {
            return;
        }
        String id_kategori = (String) tm.getValueAt(i, 0);
        txtid.setText(id_kategori);
        String nama_kategori = (String) tm.getValueAt(i, 1);
        txtnama.setText(nama_kategori);
    }//GEN-LAST:event_tblkategoriMouseClicked

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus?='"
                + txtid.getText() + "'", "punten", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                PreparedStatement ps = con.prepareStatement("DELETE FROM tbl_kategori WHERE id_kategori=?");
                ps.setString(1, txtid.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "DATA telah di Delete");
                tampil_table();
            } catch (Exception e) {
                System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

