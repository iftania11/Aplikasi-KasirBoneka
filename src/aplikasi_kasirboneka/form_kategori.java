    public void tampil_table() {
        tm = new DefaultTableModel(null,
                new Object[]{"Id kategori", "Nama kategori"});
        tblkategori.setModel(tm);
        tm.getDataVector().removeAllElements();
        try {
            PreparedStatement s = con.prepareStatement("select * from tbl_kategori");
            ResultSet r = s.executeQuery();
            while (r.next()) {
                Object[] data = {
                    r.getString(1),
                    r.getString(2)
                };
                tm.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("error koneksi database" + e);
        }
    }
    
